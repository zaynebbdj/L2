/**
 * Cette classe represente une couleur en fonction de ses valeur rgb
 */
public class Color {
    private double premier;
    private double deuxieme ;
    private double troisieme ;

    /**
     * methode qui retourne le premier parametre d'une couleur
     * @return le premier parametre d'une couleur
     */
    public double getpremier(){
        return this.premier;
    }
    /**
     * methode qui retourne le deuxieme parametre d'une couleur
     * @return le deuxieme parametre d'une couleur
     */
    public double getdeuxieme(){
        return this.deuxieme;
    }
    /**
     * retourne le troisieme prarametre d'une couleur
     * @return le troisieme parametres d'une couleur
     */
    public double gettroisieme(){
        return this.troisieme;
    }
    
}
